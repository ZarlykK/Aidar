package Loops;

public class DoWhilePractice {
    public static void main(String[] args) {
        // show ur age from 21 - 26
        int age=21;
        do{
            System.out.println("Your age is "+age);
            age++;
        }while(age<15);//in do while loop if the condition is false it will only print once
        // while condition is true it does loop
    }
}
