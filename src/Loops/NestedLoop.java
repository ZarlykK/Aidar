package Loops;

public class NestedLoop {
    public static void main(String[] args) {
        int i=0;
        for(;i<3;i++) {
            System.out.println("I am first for Loop");
            for(int j=5; j<5; j++) {
                System.out.println("I am nested for Loop");
            }




        }
    }
}
